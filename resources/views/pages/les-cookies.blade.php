				<h3 id="faq_content_7377" data-fontsize="16" data-lineheight="24">Qu’est-ce qu’un cookie ?</h3>
<div>Un cookie est un fichier texte déposé, sous réserve de vos choix, sur votre ordinateur lors de la visite d’un site ou de la consultation d’une publicité. Il a pour but de collecter des informations relatives à votre navigation et de vous adresser des services adaptés à votre terminal (ordinateur, mobile ou tablette).Les cookies sont gérés par votre navigateur internet et seul l’émetteur d’un cookie est susceptible de lire ou de modifier les informations qui y sont contenues.<p></p>
</div>
<p>&nbsp;</p>
<h3 id="faq_content_7378" data-fontsize="16" data-lineheight="24">A quoi servent les cookies émis sur notre site ?</h3>
<div>Les cookies utilisés sur notre site permettent d’identifier les services et rubriques que l’utilisateur a visités, et plus généralement son comportement en matière de visites. Ces informations sont utiles pour mieux personnaliser les services et les contenus&nbsp; qui apparaissent sur notre site et faciliter votre navigation sur notre site.<br>
Des cookies sont également nécessaires au bon fonctionnement de certains services ou encore pour mesurer leur audience, notamment Google Analytics.&nbsp;Ces entités se sont engagées à respecter la législation en vigueur quant aux cookies/traceurs et garantissent la mise en œuvre de mesures de protection et de sécurisation des données recueillies.<br>
Lorsque vous vous connectez à notre site, nous pouvons être amenés, sous réserve de vos choix, à installer divers cookies dans votre terminal nous permettant de reconnaître le navigateur de votre terminal pendant la durée de validité du cookie concerné.<br>
Les cookies que nous émettons sont utilisés aux fins décrites ci-dessus, sous réserve de vos choix qui résultent des paramètres de votre logiciel de navigation utilisé lors de votre visite de notre site.Les cookies que nous émettons ont une durée de vie maximale de 60 jours.<p></p>
<p>Vous pouvez autoriser, bloquer ou supprimer les cookies installés sur votre poste en paramétrant les options de votre navigateur. Pour les navigateurs les plus courants, vous trouverez des indications via les liens suivants :</p>
<ul translate="none">
<li><a href="https://support.mozilla.org/fr/kb/empecher-sites-enregistrer-preferences" target="_blank" rel="noopener">Firefox</a></li>
